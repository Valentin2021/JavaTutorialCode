
public class Exponents {

	public static void main(String[] args) {
		// an exponent is one number raised to the other
		// it can be an int,float,long or a double
		
		System.out.println(Math.pow(5, 2));
		
		System.out.println((int) Math.pow(5, 2)); //display without decimals
		
		double exponent = Math.pow(2, 2);
		System.out.println(exponent);
		
		int exponent2 = (int) Math.pow(2, 3); // no decimals
		System.out.println(exponent2);
	}

}
